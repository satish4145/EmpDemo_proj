package com.demo.pojo;

public class ProductPojo {
	
	int productitemno = 0;
	String producttype = null;
	String productname = null;
	int productprice = 0;
	String productwarrantydetails = null;
	public int getProductitemno() {
		return productitemno;
	}
	public void setProductitemno(int productitemno) {
		this.productitemno = productitemno;
	}
	public String getProducttype() {
		return producttype;
	}
	public void setProducttype(String producttype) {
		this.producttype = producttype;
	}
	public String getProductname() {
		return productname;
	}
	public void setProductname(String productname) {
		this.productname = productname;
	}
	public int getProductprice() {
		return productprice;
	}
	public void setProductprice(int productprice) {
		this.productprice = productprice;
	}
	public String getProductwarrantydetails() {
		return productwarrantydetails;
	}
	public void setProductwarrantydetails(String productwarrantydetails) {
		this.productwarrantydetails = productwarrantydetails;
	}
	

}
